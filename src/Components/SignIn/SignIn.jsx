import React, { useState } from 'react'
import { getAuth, signInWithEmailAndPassword } from "firebase/auth";
import {  useDispatch } from 'react-redux';
import {logIn ,logOut} from "../../../Configuration/authStore/authSlice" ;
import { Navigate, redirect } from 'react-router-dom';
import { useNavigate } from 'react-router-dom';


const SignIn = () => {

    const [ email , setEmail ] = useState("") ;
    const [ password , setPassword ] = useState("") ;
    const auth = getAuth();
    const dispatch = useDispatch() ;
    const navigate = useNavigate() ;


    const SignInUser = async () =>{
        try {
          await signInWithEmailAndPassword(auth,email,password)
          .then( (userData)=>{
            dispatch(logIn({
              authStatus:true ,
             userData :userData.email
            }))
            

          })
         navigate("/Home")
        } catch (error) {
          console.log("Firebase Error : Sign In User " ,error) ;
        }
    } ;

  return (
    <div className='container'> 
      <form onSubmit={(e)=>e.preventDefault()}>
  <div class="mb-3">
    <label htmlFor="exampleInputEmail1" class="form-label">Email address</label>
    <input type="email" class="form-control" id="exampleInputEmail1" aria-describedby="emailHelp"
    value={email}
    onChange={(e)=>setEmail(e.target.value)} 
    />
    <div id="emailHelp" class="form-text">We'll never share your email with anyone else.</div>
  </div>
  <div class="mb-3">
    <label htmlFor="exampleInputPassword1" class="form-label">Password</label>
    <input type="password" class="form-control" id="exampleInputPassword1"
    value={password}
    onChange={(e)=>setPassword(e.target.value)} 
    
    />
  </div>
  <div class="mb-3 form-check">
    <input type="checkbox" class="form-check-input" id="exampleCheck1"/>
    <label class="form-check-label" htmlFor="exampleCheck1">Check me out</label>
  </div>
  <button type="submit" onClick={SignInUser} class="btn btn-primary" >
    
    Sign In 
    
    </button>
</form>
    </div>
  )
}

export default SignIn

